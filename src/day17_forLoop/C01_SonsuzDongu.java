package day17_forLoop;

public class C01_SonsuzDongu {
    public static void main(String[] args) {

        //Bu kodu calıstıtırsak sonsuz donguye girer
        //ve ram dolup bilgisayarı kitleyene kadar calısır

        //bu yuzden loop olustururken baslangıc nokatasından sonuca dogru gidiyor muyuz kontrol etmek lazım

        for (int i = 0; i >= 0; i++) {
            System.out.println(i);   //Sonsuz dongu
        }






    }
}
